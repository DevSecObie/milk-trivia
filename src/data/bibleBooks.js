export const TOTAL_BOOKS = BIBLE_BOOKS.length

export function getBook(n) { return BIBLE_BOOKS[n - 1] }
export function getBookByName(name) { return BIBLE_BOOKS.find(b => b.name === name) }
export function getNextBook(n) { return n < TOTAL_BOOKS ? BIBLE_BOOKS[n] : null }
export function getPrevBook(n) { return n > 1 ? BIBLE_BOOKS[n - 2] : null }
export function getBooksInSection(section) { return BIBLE_BOOKS.filter(b => b.section === section) }
